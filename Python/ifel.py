n=int(input("enter the number 1"))
m=int(input("enter the number 2"))
op=input("enter the operation")
if(op=="+"):
    print("Sum = ",m+n)
elif(op=="-"):
    print("minus =",m-n)
elif(op=="/"):
    print("divide =",m/n)
elif(op=="*"):
    print("multiply =",m*n)
else:
    print("invalid operations")
    
